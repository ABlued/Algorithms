// 선택 정렬 : 가장 큰 수를 찾아내서 맨 끝에 보낸다. 그 다음 큰 수를 찾아내서 그 다음 끝에 보낸다. 이를 반복한다.
// 선택 정렬은 O(n^2)의 시간을 갖는다.

// 버블 정렬 : i번째 원소와 i + 1번째 원소를 서로 비교하여 자리를 바꾼다. 그 다음 i + 1번째와 i + 2번째도 비교한다. 이를 반복한다.
// 버블 정렬은 O(n^2)의 시간을 갖는다.

// 삽입 정렬 : 맨 첫번째 데이터는 정렬된 상태라고 가정하고 그 다음의 원소와 비교하여 자리를 바꾼다.
// 삽입 정렬은 O(n^2)의 시간을 갖는다.